<center><h3 style="color:white">TOPOLOGÍA</h3></center>
<div style="display: flex; justify-content: center;">
          <p style="color:white; text-align: justify; width: 80%;">
            Se pre estableció un diseño mediante el uso de Cisco Packet Tracer para la topología del paquete de 4 cámaras de CCTV, 
el cual se encuentran conectadas a un switch que funciona como un conmutador de red, permitiendo la comunicación de las cámaras con el DVR, 
este se conecta directamente al monitor para recibir, almacenar y gestionar las grabaciones de video para visualizarlas en tiempo real. 
Para habilitar el acceso remoto al sistema de CCTV, el conmutador está conectado a un router denominado “TELMEX” que proporciona una conexión a 
internet por medio de la nube que simula una red externa. 
</p></div>
